package com.practice;

public class Student {
	int sid;
	String name;
	char gender;
	String college;
	String address;
	float percentage;
	public Student(int sid, String name, char gender, String college, String address, float percentage) {
		super();
		this.sid = sid;
		this.name = name;
		this.gender = gender;
		this.college = college;
		this.address = address;
		this.percentage = percentage;
	}
	
	
	

}
